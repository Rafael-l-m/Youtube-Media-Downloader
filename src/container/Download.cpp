    Download& Download::operator=(const Download& other) {
        if (this != &other) {
            QMutexLocker locker(&this->mutex);

            this->id = other.id;
            this->url = other.url;
            this->audio_code = other.audio_code;
            this->video_code = other.video_code;
            this->video_format = other.video_format;
            this->title = other.title;
            this->save_name = other.save_name;
            this->file_path = other.file_path;
            this->suffix = other.suffix;
            this->progress = other.progress;
            this->download_state = other.download_state;
            this->metadata = other.metadata;
            this->subtitles = other.subtitles;
            this->auto_generated_subtitles = other.auto_generated_subtitles;
        }

        return *this;
    }

