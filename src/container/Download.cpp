    QJsonObject Download::toJson() const {
        QMutexLocker locker(&this->mutex);

        QJsonObject obj;

        obj[DB_CONSTANTS::JSON_FORMAT::USER_ID] = this->id;
        obj[DB_CONSTANTS::JSON_FORMAT::URL] = this->url;
        obj[DB_CONSTANTS::JSON_FORMAT::AUDIO_CODE] = this->audio_code;
        obj[DB_CONSTANTS::JSON_FORMAT::VIDEO_CODE] = this->video_code;
        obj[DB_CONSTANTS::JSON_FORMAT::VIDEO_FORMAT] = this->video_format;
        obj[DB_CONSTANTS::JSON_FORMAT::TITLE] = this->title;
        obj[DB_CONSTANTS::JSON_FORMAT::SAVE_NAME] = this->save_name;
        obj[DB_CONSTANTS::JSON_FORMAT::FILE_PATH] = this->file_path;
        obj[DB_CONSTANTS::JSON_FORMAT::SUFFIX] = this->suffix;
        obj[DB_CONSTANTS::JSON_FORMAT::PROGRESS] = this->progress;
        obj[DB_CONSTANTS::JSON_FORMAT::DOWNLOAD_STATE] = this->download_state;
        obj[DB_CONSTANTS::JSON_FORMAT::METADATA] = this->metadata;
        obj[DB_CONSTANTS::JSON_FORMAT::SUBTITLES] = this->subtitles;
        obj[DB_CONSTANTS::JSON_FORMAT::AUTO_GENERATED_SUBTITLES] = this->auto_generated_subtitles;

        return obj;
    }

    Download Download::fromJson(const QJsonObject& obj) {
        const auto id = obj[DB_CONSTANTS::JSON_FORMAT::USER_ID].toInt();
        const auto url = obj[DB_CONSTANTS::JSON_FORMAT::URL].toString();
        const auto ac = obj[DB_CONSTANTS::JSON_FORMAT::AUDIO_CODE].toInt();
        const auto vc = obj[DB_CONSTANTS::JSON_FORMAT::VIDEO_CODE].toInt();
        const auto vf = obj[DB_CONSTANTS::JSON_FORMAT::VIDEO_FORMAT].toString();
        const auto tt = obj[DB_CONSTANTS::JSON_FORMAT::TITLE].toString();
        const auto sn = obj[DB_CONSTANTS::JSON_FORMAT::SAVE_NAME].toString();
        const auto fp = obj[DB_CONSTANTS::JSON_FORMAT::FILE_PATH].toString();
        const auto suf = obj[DB_CONSTANTS::JSON_FORMAT::SUFFIX].toString();
        const auto prog = obj[DB_CONSTANTS::JSON_FORMAT::PROGRESS].toInt();
        const auto ds = obj[DB_CONSTANTS::JSON_FORMAT::DOWNLOAD_STATE].toBool();
        const auto met = obj[DB_CONSTANTS::JSON_FORMAT::METADATA].toBool();
        const auto subt = obj[DB_CONSTANTS::JSON_FORMAT::SUBTITLES].toBool();
        const auto a_subt = obj[DB_CONSTANTS::JSON_FORMAT::AUTO_GENERATED_SUBTITLES].toBool();

        return {id, url, ac, vc, vf, tt, sn, fp, suf, prog, ds, met, subt, a_subt};
    }

    QJsonObject Download::toJsonList(const QList<Download>& list) {
        QJsonObject obj;
        QJsonArray arr;

        for (const auto& item : list) {arr.append(item.toJson());}
        obj[DB_CONSTANTS::JSON_FORMAT::PARAMETERS] = arr;

        return obj;
    }

    QList<Download> Download::fromJsonList(const QJsonObject& obj) {
        QList<Download> list;
        for (const auto arr = obj[DB_CONSTANTS::JSON_FORMAT::PARAMETERS].toArray(); const auto& item : arr) {list.append(fromJson(item.toObject()));}
        return list;
    }
