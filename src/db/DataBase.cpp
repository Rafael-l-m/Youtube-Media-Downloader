    CONTAINER::Download DataBase::read(const int id) const {
        QSqlQuery q(this->db);

        q.prepare("SELECT id, url, audio_code, video_code, video_format, title, save_name, file_path, suffix, progress, download_state, metadata, subtitles, auto_generated_subtitles FROM downloads WHERE id = :id");
        q.bindValue(":id", id);

        if (!q.exec()) return {};

        if (q.next()) {
            const auto idd = q.value("id").toInt();
            const auto url = q.value("url").toString();
            const auto ac = q.value("audio_code").toInt();
            const auto vc = q.value("video_code").toInt();
            const auto vf = q.value("video_format").toString();
            const auto tt = q.value("title").toString();
            const auto sn = q.value("save_name").toString();
            const auto fp = q.value("file_path").toString();
            const auto suf = q.value("suffix").toString();
            const auto prog = q.value("progress").toInt();
            const auto ds = q.value("download_state").toBool();
            const auto met = q.value("metadata").toBool();
            const auto subt = q.value("subtitles").toBool();
            const auto a_subt = q.value("auto_generated_subtitles").toBool();

            return {idd, url, ac, vc, vf, tt, sn, fp, suf, prog, ds, met, subt, a_subt};
        }

        return {};
    }

    QList<CONTAINER::Download> DataBase::readAll() const {
        QList<CONTAINER::Download> list;

        QSqlQuery q(this->db);

        q.prepare("SELECT id, url, audio_code, video_code, video_format, title, save_name, file_path, suffix, progress, download_state, metadata, subtitles, auto_generated_subtitles FROM downloads");

        if (!q.exec()) return {};

        while (q.next()) {
            const auto idd = q.value("id").toInt();
            const auto url = q.value("url").toString();
            const auto ac = q.value("audio_code").toInt();
            const auto vc = q.value("video_code").toInt();
            const auto vf = q.value("video_format").toString();
            const auto tt = q.value("title").toString();
            const auto sn = q.value("save_name").toString();
            const auto fp = q.value("file_path").toString();
            const auto suf = q.value("suffix").toString();
            const auto prog = q.value("progress").toInt();
            const auto ds = q.value("download_state").toBool();
            const auto met = q.value("metadata").toBool();
            const auto subt = q.value("subtitles").toBool();
            const auto a_subt = q.value("auto_generated_subtitles").toBool();

            list.append({idd, url, ac, vc, vf, tt, sn, fp, suf, prog, ds, met, subt, a_subt});
        }

        return list;
    }


