    DataBase::DataBase(QObject* parent) : QObject(parent) {}

    bool DataBase::init() {
        this->dataBaseDir = QStandardPaths::writableLocation(QStandardPaths::AppDataLocation);

        if (const QDir dir; !dir.exists(this->dataBaseDir)) dir.mkpath(this->dataBaseDir);

        this->dataBaseDir = this->dataBaseDir + QDir::separator() + DB_CONSTANTS::DB_MANAGER::DATABASE_NAME;

        if (!QFile::exists(this->dataBaseDir)) {
            QFile file(this->dataBaseDir);

            if (!file.open(QIODevice::WriteOnly)) return false;

            file.close();

            QFile::setPermissions(this->dataBaseDir,
                QFileDevice::ReadOwner | QFileDevice::WriteOwner |
                QFileDevice::ReadUser | QFileDevice::WriteUser);
        }

        this->db = QSqlDatabase::addDatabase(DB_CONSTANTS::DB_MANAGER::CONNECTION_TYPE);
        this->db.setDatabaseName(this->dataBaseDir);

        return this->db.open() && this->createTable();
    }

    bool DataBase::isOpen() const {return this->db.isOpen();}

    bool DataBase::isEmpty() const {
        QSqlQuery q(this->db);

        q.prepare("SELECT 1 FROM downloads LIMIT 1");
        q.exec();

        return !q.next();
    }

    QString DataBase::getPath() const {return this->dataBaseDir;}

    bool DataBase::ifExists(const int id) const {
        QSqlQuery q(this->db);

        q.prepare("SELECT COUNT(*) FROM downloads WHERE id = :id");
        q.bindValue(":id", id);
        q.exec();

        return q.next() && q.value(0).toInt() > 0;
    }

    bool DataBase::add(const CONTAINER::Download& d) const {
        QSqlQuery q(this->db);

        q.prepare("INSERT INTO downloads (id, url, audio_code, video_code, video_format, title, save_name, file_path, suffix, progress, download_state, metadata, subtitles, auto_generated_subtitles)"
                  "VALUES (:id, :url, :ac, :vc, :vf, :tt, :sn, :fp, :suf, :prog, :ds, :met, :subt, :at_subt)");

        q.bindValue(":id", d.getId());
        q.bindValue(":url", d.getUrl());
        q.bindValue(":ac", d.getAudioCode());
        q.bindValue(":vc", d.getVideoCode());
        q.bindValue(":vf", d.getVideoFormat());
        q.bindValue(":tt", d.getTitle());
        q.bindValue(":sn", d.getSaveName());
        q.bindValue(":fp", d.getFilePath());
        q.bindValue(":suf", d.getSuffix());
        q.bindValue(":prog", 0);
        q.bindValue(":ds", 0);
        q.bindValue(":met", d.getMetadata() ? 1 : 0);
        q.bindValue(":subt", d.getSubtitles() ? 1 : 0);
        q.bindValue(":at_subt", d.getAutoGeneratedSubtitles() ? 1 : 0);

        if (!q.exec()) return false;

        return q.numRowsAffected() > 0;
    }

