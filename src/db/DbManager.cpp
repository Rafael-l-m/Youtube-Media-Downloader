    QJsonObject DbManager::readDownload(const QJsonObject& obj) const {
        QJsonObject res;

        if (const auto userId = obj[DB_CONSTANTS::JSON_FORMAT::USER_ID].toInt(); !ifExists(userId)) {
            res[DB_CONSTANTS::JSON_FORMAT::STATUS] = DB_CONSTANTS::JSON_FORMAT::STATUS_REFUSED;
            res[DB_CONSTANTS::JSON_FORMAT::MESSAGE] = DB_CONSTANTS::JSON_FORMAT::ID_NOT_EXISTS();
        }

        else {
            res[DB_CONSTANTS::JSON_FORMAT::STATUS] = DB_CONSTANTS::JSON_FORMAT::STATUS_OK;
            res[DB_CONSTANTS::JSON_FORMAT::PARAMETERS] = read(userId).toJson();
        }

        return res;
    }

    QJsonObject DbManager::readAllDownloads() const {
        QJsonObject res;

        if (isEmpty()) {
            res[DB_CONSTANTS::JSON_FORMAT::STATUS] = DB_CONSTANTS::JSON_FORMAT::STATUS_REFUSED;
            res[DB_CONSTANTS::JSON_FORMAT::MESSAGE] = DB_CONSTANTS::JSON_FORMAT::DATABASE_EMPTY();
        }

        else {
            res[DB_CONSTANTS::JSON_FORMAT::STATUS] = DB_CONSTANTS::JSON_FORMAT::STATUS_OK;
            res[DB_CONSTANTS::JSON_FORMAT::PARAMETERS] = CONTAINER::Download::toJsonList(readAll());
        }

        return res;
    }
