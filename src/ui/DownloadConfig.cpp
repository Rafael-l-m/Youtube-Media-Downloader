    void DownloadConfig::requestEdit(const CONTAINER::Download& download) {
        this->normalMode = false;
        this->askForSavePath = false;

        this->idd = download.getId();
        this->url = download.getUrl();
        this->audioCode = download.getAudioCode();
        this->videoCode = download.getVideoCode();
        this->videoFormat = download.getVideoFormat();
        this->title = download.getTitle();
        this->filePath = download.getFilePath();
        this->saveName = download.getSaveName();
        this->suffix = download.getSuffix();
        this->progress = download.getProgress();
        this->downloadState = download.getDownloadState();
        this->metadata = download.getMetadata();
        //this->subtitles = download.getSubtitles();
        //this->autoGeneratedSubtitles = download.getAutoGeneratedSubtitles();

        this->urlEdit->setText(this->url);
        this->urlEdit->setEnabled(false);
        this->audioEdit->setText(this->audioCode == 0 ? QString() : QString::number(this->audioCode));
        this->videoEdit->setText(this->videoCode == 0 ? QString() : QString::number(this->videoCode));
        this->nonStandardVideoEdit->setText(this->videoFormat.trimmed().isEmpty() ? QString() : this->videoFormat.trimmed());
        this->titleEdit->setText(this->title);
        this->metCombo->setCurrentText(this->metadata ? UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_METADATA() : UI_CONSTANTS::DOWNLOAD_CONFIG::WITHOUT_METADATA());
        //this->subtCombo->setCurrentText(this->subtitles ? UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_SUBTITLES() : UI_CONSTANTS::DOWNLOAD_CONFIG::WITHOUT_SUBTITLES());
        //this->at_subt_combo->setCurrentText(this->autoGeneratedSubtitles ? UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_AUTO_GENERATED_SUBTITLES() : UI_CONSTANTS::DOWNLOAD_CONFIG::WITHOUT_AUTO_GENERATED_SUBTITLES());
        this->outputEdit->setText(this->filePath + QDir::separator() + this->saveName + UI_CONSTANTS::MAIN_WINDOW::POINT + this->suffix);

        if (this->audioCode == 0 && this->videoCode == 0) {
            if (CORE_CONSTANTS::MEDIA_FILTERS::AUDIO_FILTERS::SUPPORTED_AUDIO_FORMATS.contains(this->suffix)) this->modeCombo->setCurrentText(UI_CONSTANTS::DOWNLOAD_CONFIG::BEST_AUDIO());

            else this->modeCombo->setCurrentText(UI_CONSTANTS::DOWNLOAD_CONFIG::BEST_VIDEO());
        }

        else this->modeCombo->setCurrentText(UI_CONSTANTS::DOWNLOAD_CONFIG::PERSONALIZED());
    }


