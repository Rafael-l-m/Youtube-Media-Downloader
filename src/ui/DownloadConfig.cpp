    void DownloadConfig::timer1Finished(const QString& text) {
        if (this->normalMode) {
            if (const auto text_t = text.trimmed(); !text_t.isEmpty()) {
                if (CORE::MEDIA_INFO::ifValidURL(text_t)) {
                    this->url = CORE::MEDIA_INFO::cutURL(text_t);

                    const auto tools = CORE::SharedStorage::instance().getToolsPath();

                    this->showITagsBtn->setEnabled(false);
                    this->urlEdit->setEnabled(false);
                    this->modeCombo->setEnabled(false);
                    this->metCombo->setEnabled(false);
                    //this->subtCombo->setEnabled(false);
                    //this->at_subt_combo->setEnabled(false);
                    this->audioEdit->setEnabled(false);
                    this->videoEdit->setEnabled(false);
                    this->nonStandardVideoEdit->setEnabled(false);
                    this->outputBtn->setEnabled(false);

                    sleepForMilliseconds(UI_CONSTANTS::DOWNLOAD_CONFIG::SLEEP_TIME);

                    try {
                        if (const auto tt = CORE::MEDIA_INFO::getTitleFromURL(tools->getYtDlpPath(), tools->getFFmpegPath(), this->url); !tt.isEmpty()) {
                            this->titleEdit->setText(tt);
                            this->title = tt;
                        }

                        else {
                            this->titleEdit->setText(UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED());
                            this->title = UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED();
                        }

                    }

                    catch (...) {
                        this->titleEdit->setText(UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED());
                        this->title = UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED();
                    }

                    this->showITagsBtn->setEnabled(true);
                    this->urlEdit->setEnabled(true);
                    this->modeCombo->setEnabled(true);
                    this->metCombo->setEnabled(true);
                    //this->subtCombo->setEnabled(true);
                    //this->at_subt_combo->setEnabled(true);
                    this->audioEdit->setEnabled(true);
                    this->videoEdit->setEnabled(true);
                    this->nonStandardVideoEdit->setEnabled(true);
                    this->outputBtn->setEnabled(true);
                }

                else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_URL(text_t));
            }

            else {
                this->url = QString();
                this->title = QString();
                this->titleEdit->setText(QString());
            }
        }
    }

    void DownloadConfig::timer2Finished(const QString& audio_code) {
        if (const auto act = audio_code.trimmed(); !act.isEmpty()) {
            if (const auto ac = act.toInt(); CORE::MEDIA_INFO::ifValidAudioCode(ac)) this->audioCode = ac;

            else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_AUDIO_I_TAG(act));
        }

        else this->audioCode = 0;
    }

    void DownloadConfig::timer3Finished(const QString& video_code) {
        if (const auto vct = video_code.trimmed(); !vct.isEmpty()) {
            if (const auto vc = vct.toInt(); CORE::MEDIA_INFO::ifValidVideoCode(vc)) this->videoCode = vc;

            else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_VIDEO_I_TAG(vct));
        }

        else this->videoCode = 0;
    }

    void DownloadConfig::timer4Finished(const QString& video_format) {
        if (const auto vf = video_format.trimmed(); !vf.isEmpty()) {
            if (CORE::MEDIA_INFO::ifValidVideoFormat(vf)) this->videoFormat = vf;

            else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_NON_STANDARD_VIDEO_I_TAG(vf));
        }

        else this->videoFormat = QString();
    }

    void DownloadConfig::showItagsBtnClicked() const {
        if (const auto tt = this->titleEdit->text().trimmed(); tt.isEmpty() || tt.compare(UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED(), Qt::CaseInsensitive) == 0) NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_URL(this->urlEdit->text().trimmed()));

        else {
            auto* show = new ShowItags(this->url);
            show->setAttribute(Qt::WA_DeleteOnClose);
            show->exec();
        }
    }

