    void DownloadConfig::outputBtnClicked() {
        if (CORE::MEDIA_INFO::ifValidURL(this->url) && this->title.compare(UI_CONSTANTS::DOWNLOAD_CONFIG::TITLE_FAILED(), Qt::CaseInsensitive) != 0) {
            if (const auto m = this->modeCombo->currentText(); m.compare(UI_CONSTANTS::DOWNLOAD_CONFIG::BEST_AUDIO(), Qt::CaseInsensitive) == 0) {
                this->ifAudio = UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                this->audioCode = 0;
                this->videoCode = 0;
                this->videoFormat = QString();
            }

            else if (m.compare(UI_CONSTANTS::DOWNLOAD_CONFIG::BEST_VIDEO(), Qt::CaseInsensitive) == 0) {
                this->ifAudio = !UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                this->audioCode = 0;
                this->videoCode = 0;
                this->videoFormat = QString();
            }

            else if (m.compare(UI_CONSTANTS::DOWNLOAD_CONFIG::PERSONALIZED(), Qt::CaseInsensitive) == 0) {
                this->timer3Finished(this->videoEdit->text().trimmed());

                if (CORE::MEDIA_INFO::ifValidAudioCode(this->audioCode)) {
                    const auto f1 = CORE::MEDIA_INFO::ifValidVideoCode(this->videoCode);
                    const auto f2 = CORE::MEDIA_INFO::ifValidVideoFormat(this->videoFormat.trimmed());

                    if (f1 && !this->videoFormat.isEmpty() && f2) {
                        if (NoticeView::confirmation(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::CHOOSE_BETWEEN_VI_AND_VI_AI())) {
                            this->ifAudio = !UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                            this->videoCode = 0;
                        }

                        else return;
                    }

                    else if (f1) {
                        this->ifAudio = !UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                        this->videoFormat = QString();
                    }

                    else if (!this->videoFormat.isEmpty() && f2) {
                        this->ifAudio = !UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                        this->videoCode = 0;
                    }

                    else {
                        this->ifAudio = UI_CONSTANTS::SETTINGS_VIEW::DEFAULT_IF_AUDIO;
                        this->videoCode = 0;
                        this->videoFormat = QString();
                    }
                }

                else {
                    NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::REQUIRED_AUDIO_I_TAG());
                    return;
                }
            }

            this->metadata = this->metCombo->currentText().compare(UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_METADATA(), Qt::CaseInsensitive) == 0;
            //this->subtitles = this->subtCombo->currentText().compare(UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_SUBTITLES(), Qt::CaseInsensitive) == 0;
            this->subtitles = false;
            //this->autoGeneratedSubtitles = this->at_subt_combo->currentText().compare(UI_CONSTANTS::DOWNLOAD_CONFIG::WITH_AUTO_GENERATED_SUBTITLES(), Qt::CaseInsensitive) == 0;
            this->autoGeneratedSubtitles = false;

            if (this->askForSavePath) {if (!NoticeView::confirmation(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::ASK_FOR_SAVE_PATH())) return; this->askForSavePath = false;}

            this->urlEdit->setEnabled(false);
            this->modeCombo->setEnabled(false);
            this->showITagsBtn->setEnabled(false);
            this->audioEdit->setEnabled(false);
            this->videoEdit->setEnabled(false);
            this->nonStandardVideoEdit->setEnabled(false);
            this->metCombo->setEnabled(false);
            //this->subtCombo->setEnabled(false);
            //this->at_subt_combo->setEnabled(false);

            QFileDialog dialog {
                this,
                UI_CONSTANTS::DOWNLOAD_CONFIG::SAVE_AS(),
                QStandardPaths::writableLocation(QStandardPaths::DownloadLocation) + QDir::separator() + this->title,
                this->ifAudio ? CORE_CONSTANTS::MEDIA_FILTERS::AUDIO_FILTERS::FILTER_AUDIO : CORE_CONSTANTS::MEDIA_FILTERS::VIDEO_FILTERS::FILTER_VIDEO,
            };

            #ifdef Q_OS_MAC
                dialog.setOptions(QFileDialog::DontUseNativeDialog);
            #elif defined(Q_OS_WIN)
            #elif defined(Q_OS_LINUX)
                dialog.setFileMode(QFileDialog::AnyFile);
                dialog.setOption(QFileDialog::ShowDirsOnly, false);
                dialog.setAcceptMode(QFileDialog::AcceptSave);
            #endif

            dialog.setFilter(dialog.filter() | QDir::Hidden | QDir::NoDotAndDotDot);

            if (dialog.exec() == Accepted) {
                if (const auto fullPath = dialog.selectedFiles().first(); !fullPath.isEmpty()) {
                    const auto folder = QFileInfo(fullPath).absolutePath();
                    const auto fileName = QFileInfo(fullPath).completeBaseName();
                    const auto selectedFilter = dialog.selectedNameFilter();
                    const auto suf = this->ifAudio ? CORE_CONSTANTS::MEDIA_FILTERS::AUDIO_FILTERS::EXTRACT_AUDIO_FILTER(selectedFilter) : CORE_CONSTANTS::MEDIA_FILTERS::VIDEO_FILTERS::EXTRACT_VIDEO_FILTER(selectedFilter);

                    if (CORE::MEDIA_INFO::ifValidPath(folder)) {
                        if (CORE::MEDIA_INFO::ifValidSaveName(fileName)) {
                            if (CORE::MEDIA_INFO::ifValidSuffix(suf)) {
                                this->okBtn->setEnabled(true);
                                this->filePath = folder;
                                this->saveName = fileName;
                                this->suffix = suf;
                                this->outputEdit->setText(this->filePath + QDir::separator() + this->saveName + UI_CONSTANTS::MAIN_WINDOW::POINT + this->suffix);
                            }

                            else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_SUFFIX(suf));
                        }

                        else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_SAVE_NAME(fileName));
                    }

                    else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::INVALID_FILE_PATH(folder));
                }

                else {
                    this->outputEdit->setText(QString());
                    this->filePath = QString();
                    this->saveName = QString();
                    this->suffix = QString();
                }
            }
        }

        else NoticeView::warning(UI_CONSTANTS::DOWNLOAD_CONFIG::WARNING(), UI_CONSTANTS::DOWNLOAD_CONFIG::REQUIRED_URL());
    }

